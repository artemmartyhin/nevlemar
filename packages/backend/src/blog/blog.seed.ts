@Injectable()
export class BlogSeedService implements OnModuleInit {
  private readonly logger = new Logger(BlogSeedService.name);

  constructor(
    @InjectModel('BlogPost') private readonly post: Model<BlogPost>,
    @InjectModel('BlogCategory') private readonly category: Model<BlogCategory>,
  ) {}

  async onModuleInit() {
    try {
      const catCount = await this.category.countDocuments();
      if (catCount === 0) {
        await this.category.insertMany(CATEGORIES);
        this.logger.log(`Seeded ${CATEGORIES.length} blog categories`);
      }
      const postCount = await this.post.countDocuments();
      if (postCount === 0) {
        for (const p of POSTS) {
          const words = p.content.replace(/<[^>]*>/g, ' ').split(/\s+/).length;
          await new this.post({
            ...p,
            readingTime: Math.max(1, Math.round(words / 200)),
            published: true,
            author: 'Nevlemar',
          }).save();
        }
        this.logger.log(`Seeded ${POSTS.length} blog posts`);
      }
    } catch (e: any) {
      this.logger.warn(`Blog seed skipped: ${e.message}`);
    }
  }
}
